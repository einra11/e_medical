     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $prescription = new Prescription;
        $prescription ->patient_id=$request->input('patient_id');
        $prescription ->generic = $request->input('generic');
        $prescription ->brand = $request->input('brand');
        $prescription ->strength = $request->input('strength');
        $prescription ->form = $request->input('form');

        $prescription->save();

        return redirect('/patient/'.$request->input('patient_id'))->with('success', 'Message Sent');
    }

