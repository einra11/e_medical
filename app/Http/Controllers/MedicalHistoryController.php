    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        // dd($request);
        $this->validate($request,[
            'disease' => 'required',
            'hospital' => 'required',
            'date' => 'required',
        ]);

        $medicalHistory = new MedicalHistory;
        $medicalHistory ->patient_id=$request->input('patient_id');
        $medicalHistory ->disease = $request->input('disease');
        $medicalHistory ->hospital = $request->input('hospital');
        $medicalHistory ->date_admitted = $request->input('date');

        $medicalHistory->save();

        return redirect('/patient/'.$request->input('patient_id'))->with('success', 'Message Sent');
    }

